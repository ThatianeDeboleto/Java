
public class Main {

	public static void main(String[] args) {
		//Processamento de dados em Java, Casting
		
		int x, y;
		
		x = 5;
		y = 6;
		
		System.out.println(x);
		System.out.println(y);

		int u;
		double z;
		
		u = 10;
		z = 12 * u;
		
		System.out.println(u);
		System.out.println(z);
		
		//calculo trapezio
		
		double b, B, h, area;
		
		b = 6.0;
		B = 8.0;
		h = 5.0;
		
		area = (b + B) / 2.0 * h;
		System.out.println(area);
		
		
	double c;
	int d;
	
	c = 5.0;
	d = (int) c;
	System.out.println(d);
	
	}

}
